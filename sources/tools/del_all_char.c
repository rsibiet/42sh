void	del_all_char(char *line, char c)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (line[i])
	{
		while (line[j] == c)
			j++;
		line[i] = line[j];
		i++;
		if (line[j])
			j++;
	}
	line[i] = '\0';
}
